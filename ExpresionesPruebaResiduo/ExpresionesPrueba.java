public class ExpresionesPrueba{
	public static void main(String args[]){
		System.out.println("7 / 2 = " + (7 / 2));
		System.out.println("7 % 2 = " + (7 % 2));
		System.out.println("8 / 12 = " + (8 / 12)); // imprime el cociente
		System.out.println("8 % 12 = " + (8 % 12)); // imprime el residuo
	}
}